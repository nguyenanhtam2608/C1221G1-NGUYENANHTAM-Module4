package com.codegym.model;

public class YTe {
    String ten;
    String ngaySinh ;
    String gioiTinh;
    String quocTich;
    String cmnd;
    String phuongTien;
    String soHieu;
    String soGhe;
    String ngayDi;
    String ngayDen;
    String noiDiQua;

    public YTe(String ten, String ngaySinh, String gioiTinh, String quocTich, String cmnd, String phuongTien, String soHieu, String soGhe, String ngayDi, String ngayDen, String noiDiQua) {
        this.ten = ten;
        this.ngaySinh = ngaySinh;
        this.gioiTinh = gioiTinh;
        this.quocTich = quocTich;
        this.cmnd = cmnd;
        this.phuongTien = phuongTien;
        this.soHieu = soHieu;
        this.soGhe = soGhe;
        this.ngayDi = ngayDi;
        this.ngayDen = ngayDen;
        this.noiDiQua = noiDiQua;
    }

    public YTe() {
    }

    public String getTen() {
        return ten;
    }

    public void setTen(String ten) {
        this.ten = ten;
    }

    public String getNgaySinh() {
        return ngaySinh;
    }

    public void setNgaySinh(String ngaySinh) {
        this.ngaySinh = ngaySinh;
    }

    public String getGioiTinh() {
        return gioiTinh;
    }

    public void setGioiTinh(String gioiTinh) {
        this.gioiTinh = gioiTinh;
    }

    public String getQuocTich() {
        return quocTich;
    }

    public void setQuocTich(String quocTich) {
        this.quocTich = quocTich;
    }

    public String getCmnd() {
        return cmnd;
    }

    public void setCmnd(String cmnd) {
        this.cmnd = cmnd;
    }

    public String getPhuongTien() {
        return phuongTien;
    }

    public void setPhuongTien(String phuongTien) {
        this.phuongTien = phuongTien;
    }

    public String getSoHieu() {
        return soHieu;
    }

    public void setSoHieu(String soHieu) {
        this.soHieu = soHieu;
    }

    public String getSoGhe() {
        return soGhe;
    }

    public void setSoGhe(String soGhe) {
        this.soGhe = soGhe;
    }

    public String getNgayDi() {
        return ngayDi;
    }

    public void setNgayDi(String ngayDi) {
        this.ngayDi = ngayDi;
    }

    public String getNgayDen() {
        return ngayDen;
    }

    public void setNgayDen(String ngayDen) {
        this.ngayDen = ngayDen;
    }

    public String getNoiDiQua() {
        return noiDiQua;
    }

    public void setNoiDiQua(String noiDiQua) {
        this.noiDiQua = noiDiQua;
    }
}
